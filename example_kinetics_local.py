# %% Directories, paths, and imports. You should not need to change anything.
import os
import shutil
import sys

baseDir = os.path.dirname(os.path.abspath(__file__))
opensimADDir = os.path.join(baseDir, 'UtilsDynamicSimulations', 'OpenSimAD')
if baseDir not in sys.path:
    sys.path.append(baseDir)
if opensimADDir not in sys.path:
    sys.path.append(opensimADDir)

from UtilsDynamicSimulations.OpenSimAD.utilsOpenSimAD import processInputsOpenSimAD, plotResultsOpenSimAD
from UtilsDynamicSimulations.OpenSimAD.mainOpenSimAD import run_tracking

# %% User inputs.
'''
Please provide:

    localSessionDir:
                    Path to the local session folder generated by opencap-core.
                    This folder should contain sessionMetadata.yaml and
                    OpenSimData.

    trial_name:     Name of the trial you want to simulate. The script expects
                    to find:
                    localSessionDir/OpenSimData/Kinematics/<trial_name>.mot

    motion_type:    Type of activity you want to simulate. Options are
                    'running', 'walking', 'drop_jump', 'sit_to_stand',
                    'squats', 'jumping', and 'other'.

    time_window:    Time interval you want to simulate. It is recommended to
                    simulate trials shorter than 2s. Set to [] to simulate the
                    full trial. For 'squats' or 'sit_to_stand', you can instead
                    provide repetition below.

    repetition:     Only if motion_type is 'sit_to_stand' or 'squats'. This is
                    the index of the repetition you want to simulate, where 0 is
                    the first repetition. Set to None if you use time_window.

    case:           String appended to result file names.

    treadmill_speed:
                    Optional treadmill speed in m/s. Use 0 for overground.

    contact_side:   Foot/feet with contact spheres. Options are 'all', 'left',
                    and 'right'.
'''

# Path to your local opencap-core session results. These files are used as the
# source inputs only; results are written to outputDataFolder below.
localDataFolder = r'C:\Users\wagnerel85475\Documents\opencap-core\Data'
localSessionDir = os.path.join(localDataFolder,
                               '4d5c3eb1-1a59-4ea1-9178-d3634610561c')

# Folder where OpenSimAD will run and write Dynamics results.
outputDataFolder = os.path.join(baseDir, 'Data')

# Select the trial and simulation settings.
trial_name = '067ae22c-8944-41d5-adad-f8b4c92a47c0'
motion_type = 'squats'
time_window = [1.8, 3.6]
repetition = None
case = 'local'
treadmill_speed = 0
contact_side = 'all'

# Set to True to solve the optimal control problem.
solveProblem = True
# Set to True to analyze the results of the optimal control problem. If you
# solved the problem already, and only want to analyze/process the results, you
# can set solveProblem to False and run this script with analyzeResults set to
# True. This is useful if you do additional post-processing but do not want to
# re-run the problem.
analyzeResults = True

# Set to True to plot results.
plotResults = True

# %% Setup.
# Stage the local session into outputDataFolder. The OpenSimAD functions expect
# the same Data/<session_id> layout as example_kinetics.py and write results
# relative to dataFolder.
sourceSessionDir = os.path.abspath(localSessionDir)
dataFolder = os.path.abspath(outputDataFolder)
session_id = os.path.basename(sourceSessionDir)
localSessionDir = os.path.join(dataFolder, session_id)

sourceMetadata = os.path.join(sourceSessionDir, 'sessionMetadata.yaml')
sourceKinematics = os.path.join(sourceSessionDir, 'OpenSimData', 'Kinematics',
                                trial_name + '.mot')
sourceModelFolder = os.path.join(sourceSessionDir, 'OpenSimData', 'Model')

if not os.path.exists(sourceMetadata):
    raise FileNotFoundError('Missing local metadata file: ' + sourceMetadata)
if not os.path.exists(sourceKinematics):
    raise FileNotFoundError('Missing local kinematics file: ' + sourceKinematics)
if not os.path.exists(sourceModelFolder):
    raise FileNotFoundError('Missing local model folder: ' + sourceModelFolder)

pathMetadata = os.path.join(localSessionDir, 'sessionMetadata.yaml')
pathKinematics = os.path.join(localSessionDir, 'OpenSimData', 'Kinematics',
                              trial_name + '.mot')
pathModelFolder = os.path.join(localSessionDir, 'OpenSimData', 'Model')

os.makedirs(os.path.dirname(pathKinematics), exist_ok=True)
shutil.copy2(sourceMetadata, pathMetadata)
shutil.copy2(sourceKinematics, pathKinematics)
shutil.copytree(sourceModelFolder, pathModelFolder, dirs_exist_ok=True)

settings = processInputsOpenSimAD(baseDir, dataFolder, session_id, trial_name,
                                  motion_type, time_window, repetition,
                                  treadmill_speed, contact_side,
                                  overwrite=False)

# %% Simulation.
run_tracking(baseDir, dataFolder, session_id, settings, case=case,
             solveProblem=solveProblem, analyzeResults=analyzeResults)

# %% Plots.
# To compare different cases, add to the cases list, eg cases=['local','1'].
if plotResults:
    plotResultsOpenSimAD(dataFolder, session_id, trial_name, settings,
                         cases=[case])
